<script setup>
const route = useRoute();

const { data: page } = await useAsyncData("page-" + route.path, () => {
  return queryCollection("content").path(route.path).first();
});

if (!page.value) {
  throw createError({ fatal: true, statusCode: 404, statusMessage: "Page not found" });
}
</script>

<template>
	<ContentRenderer
		v-if="page"
		:value="page"
	/>
</template>
